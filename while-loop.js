// this loop with continue and stop

// * FOR CONTINUE WE USE 2 SAME ADDER AS LIKE X TWO TIMES ON INSIDE CONTINUE ANOTHER ONE AT THE END

x = 1;
while (x <= 10) {

    if (x == 2) {
        console.log(x);
        x++
        continue
    }


    if (x == 3) {
        console.log("lucky number " + x);

    }


    x++

    if (x == 5) {
        console.log("loop done");
        stop
    }






}

