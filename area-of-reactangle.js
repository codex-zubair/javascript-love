// Length = 15 cm

// Width = 4 cm

// Area of a rectangle = Length × Width

// 15 × 4  = 60

// So the area of rectangle = 60 cm2




function area(l, w) {
    console.log("Rectangle Length: " + l + "cm" + " Rectangle Width: " + w + "cm")
    console.log("area of rectangle: " + l * w + "cm2");
}



area(15, 4);